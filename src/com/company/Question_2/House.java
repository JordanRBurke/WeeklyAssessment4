package com.company.Question_2;

public class House {

    double houseHeight;
    double houseWidth;
    int houseYear;


    public House() {

        this.houseHeight = houseHeight;
        this.houseWidth = houseWidth;
        this.houseYear = houseYear;
    }

    public double getHouseHeight() {
        return houseHeight;
    }

    public void setHouseHeight(double houseHeight) {
        this.houseHeight = houseHeight;
    }

    public double getHouseWidth() {
        return houseWidth;
    }

    public void setHouseWidth(double houseWidth) {
        this.houseWidth = houseWidth;
    }

    public int getHouseYear() {
        return houseYear;
    }

    public void setHouseYear(int houseYear) {
        this.houseYear = houseYear;
    }














    }






